package o1_datatypes_and_varaibles;

public class Varibles_and_datatypes {
	public static void main(String[] args) {
	        System.out.println("Hello world");
	        // eight data types in java
	        // variable declaration and initialization in one line
	        int a = 10;
	        float f= 23.33f;
	        double d = 23423.43;
	        char ch = 'a';
	        String s = "hello world of java.";
	        String s1 = new String("Hello from new operator.");
	        boolean result = false;
	        short st = 1;
	        byte be = 3;
	        long  l1 = 1234567891;
	        System.out.println(a);
	        System.out.println(f);
	        System.out.println(d);
	        System.out.println(ch);
	        System.out.println(s);
	        System.out.println(s1);
	        System.out.println(result);
	        System.out.println(st);
	        System.out.println(be);
	        System.out.println(l1);
	}
}